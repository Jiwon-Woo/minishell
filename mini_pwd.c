#include "minishell.h"

int	mini_pwd(void)
{
	char	*pwd;

	pwd = getcwd(NULL, 0);
	printf("%s\n", pwd);
	if (pwd != 0)
		free(pwd);
	pwd = 0;
	return (0);
}
